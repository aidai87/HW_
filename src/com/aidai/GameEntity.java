package com.aidai;

public class GameEntity {
    private int life, damage;

    public int getLife() {
        return life;
    }

    public int getDamage() {
        return damage;
    }

    public void setLife(int life) {
        this.life = life;
    }

    public void setDamage(int damage) {
        this.damage = damage;
    }
}
